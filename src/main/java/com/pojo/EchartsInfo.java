package com.pojo;

public class EchartsInfo {

    private Integer productid;

    private Integer echartsID;

    private String productname;

    private Integer productnum;

    private String echartsName;

    private Double echartsHeight;

    private Double echartsWidth;

    private Double posLeft;

    private Double posTop;

    public Integer getProductid() {
        return productid;
    }

    public void setProductid(Integer productid) {
        this.productid = productid;
    }

    public Integer getEchartsID() {
        return echartsID;
    }

    public void setEchartsID(Integer echartsID) {
        this.echartsID = echartsID;
    }

    public String getProductname() {
        return productname;
    }

    public void setProductname(String productname) {
        this.productname = productname;
    }

    public Integer getProductnum() {
        return productnum;
    }

    public void setProductnum(Integer productnum) {
        this.productnum = productnum;
    }

    public String getEchartsName() {
        return echartsName;
    }

    public void setEchartsName(String echartsName) {
        this.echartsName = echartsName;
    }

    public Double getEchartsHeight() {
        return echartsHeight;
    }

    public void setEchartsHeight(Double echartsHeight) {
        this.echartsHeight = echartsHeight;
    }

    public Double getEchartsWidth() {
        return echartsWidth;
    }

    public void setEchartsWidth(Double echartsWidth) {
        this.echartsWidth = echartsWidth;
    }

    public Double getPosLeft() {
        return posLeft;
    }

    public void setPosLeft(Double posLeft) {
        this.posLeft = posLeft;
    }

    public Double getPosTop() {
        return posTop;
    }

    public void setPosTop(Double posTop) {
        this.posTop = posTop;
    }
}
