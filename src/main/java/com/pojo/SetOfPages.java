package com.pojo;

public class SetOfPages {
    private int SetID;
    private int PageId;
    private int PageType;
    private int PageDescribe;

    public int getSetID() {
        return SetID;
    }

    public void setSetID(int setID) {
        SetID = setID;
    }

    public int getPageId() {
        return PageId;
    }

    public void setPageId(int pageId) {
        PageId = pageId;
    }

    public int getPageType() {
        return PageType;
    }

    public void setPageType(int pageType) {
        PageType = pageType;
    }

    public int getPageDescribe() {
        return PageDescribe;
    }

    public void setPageDescribe(int pageDescribe) {
        PageDescribe = pageDescribe;
    }
}